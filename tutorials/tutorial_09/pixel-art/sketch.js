function drawPixelArt(grid, x, y, pix) {
    let i=0;
    while (i < grid.length) {
        drawRow(grid[i], x, y, pix);
        i++;
        y+=pix;
    };
}

function drawRow (row, topX, topY, pixelWidth) {

    let i = 0;
    while ( i < row.length) {
        if (row[i] == 0) {
            noFill();
        } else if (row[i] == 1) {
            fill('hotpink');
        } else if (row[i] == 2) {
            fill('black');
        } else if (row[i] == 3) {
            fill('white');
        };
        square(topX, topY, pixelWidth)
        topX += pixelWidth;
        i++;
    }
}